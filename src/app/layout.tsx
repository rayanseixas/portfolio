import { headers } from 'next/headers';

import ConditionalRootLayout from '../compenents/conditional-root-layout';

const primary = Inter({
  variable: '--font-primary',
  subsets: ['latin'],
  display: 'swap',
});

const secondary = Open_Sans({
  weight: '400',
  variable: '--font-secondary',
  subsets: ['latin'],
  display: 'swap',
});

