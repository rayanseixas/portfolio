const tertiary = localFont({
  src: [
    { path: '../assets/fonts/Dauphin.ttf', weight: '400', style: 'normal' },
  ],
  variable: '--font-tertiary',
  display: 'swap',
});

export const metadata: Metadata = {
  title: 'Portf√≥lio',
  description: 'Generated by create next app',
};

function isCleanArticleRoute(pathname: string): boolean {
  return Boolean(pathname.match(/^\/cases\/[^/]+$/));
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const headersList = await headers();
  const pathname = headersList.get('x-pathname') ?? '';
  const isClean = isCleanArticleRoute(pathname);

  const bodyClass = isClean
    ? `${primary.variable} ${secondary.variable} ${tertiary.variable} flex h-screen w-screen flex-col antialiased`
    : `flex h-screen w-screen flex-col gap-3 p-4 pb-2 ${primary.variable} ${secondary.variable} ${tertiary.variable} antialiased`;

  return (
    <html lang="pt-BR">
      <body className={bodyClass}>
        <ConditionalRootLayout>{children}</ConditionalRootLayout>
      </body>
    </html>
  );
}
